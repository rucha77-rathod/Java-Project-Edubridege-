//Write a program to calculate the sum of first 10 natural number.

public class Sum_of_natural_A4 
{
	public static void main(String args[]) {
		int i, num = 10, sum = 0;
		for (i = 1; i <= num; i++) {
			sum = sum + i;

		}
		System.out.println("sum of first 10 number is:" + sum);

	}

}
