//Write your own program using arithmetic assignment operators?


import java.util.Scanner;
public class AssignmentOp_A5 {
	public static void main(String args[]) {
		int a = 0;
	Scanner scan=new Scanner(System.in);
        
		System.out.println(a);
		a=scan.nextInt();
		a += 2;
		System.out.println(a);
		a -= 2;// a=a-10
		System.out.println(a);
		a *= 2;
		System.out.println(a);
		a /= 2;
		System.out.println(a);
		a %= 2;
		System.out.println(a);

	}

}
